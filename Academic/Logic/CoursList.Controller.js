	 var app  = angular.module('ThrillAcademic.coursList', ['ThrillAcademic.coursLogic'
			
			 , 'ngCordova'
			 , 'ThrillFrameworkLibrary.geo'
			 , 'ThrillFrameworkLibrary.Network'
			 , 'ThrillCnnWebClient.appConfig'
			 , 'ThrillFrameworkLibrary.appLogger'
	])
    /*Setup cours Controller */
	 app.controller('CoursListController', function ($scope, $http, coursLogic,  appConfig, appLogger) {
		 getLabels(appConfig.CULTURE_NAME);
		 getMessages(appConfig.CULTURE_NAME);

/*get labels with selected language*/
    function getLabels(cultureName) {
        var currentFileName = "CoursList";
        $http.get("Academic/Languages/" + currentFileName + "." + cultureName + ".json").then(function (response) {
            bindLabels(response.data);

        });
    }

    function getMessages(cultureName) {
        var alertMessageName = "AlertMessages";

        $http.get("Academic/Languages/" + alertMessageName + "." + cultureName + ".json").then(function (response) {
            $scope.alertMessageLabels = response.data.messages;
        });
    }

    /*bind labels with selected language */
	 function bindLabels(data) {
		
	 $scope.labelsCoursList = data.labels;

};
		 var refresh = function () {
			 coursLogic.getAllCourses().then(function (response) {
				 $scope.coursCollection = response;
				 $scope.sortColumn = "";
				 $scope.reverseSort = false;
		 $scope.sortData = function (column) {
			 $scope.reverseSort = ($scope.sortColumn == column) ?
				 !$scope.reverseSort : false;
			 $scope.sortColumn = column;
}
			 $scope.getSortClass = function (column) {
				 if ($scope.sortColumn == column) {
					 return $scope.reverseSort ? 'arrow-down' : 'arrow-up';
				 }
			 return '';
		 }, function (err) {
			 appLogger.error('ERR', err);
			 };
		 });
	 }
		 refresh();


	 /*Method for calling  deleting   Cours*/
	 $scope.deleteCours = function (coursEntityKey) {
            var del = confirm("Are you sure you want to Delete ?");
   if(del==true)
     {
		 coursLogic.deleteCours(coursEntityKey).then(function (response) {
			 appLogger.alert($scope.alertMessageLabels.coursDeleted);
		 refresh();
			 }, function (err) {
				 appLogger.error('ERR', err);
			 });
      }
		 };

}); // End of App Controller

